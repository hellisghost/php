let letra1 = parseInt(prompt("ingrese el numero a: "))
let letra2 = parseInt(prompt("ingrese el numero b: "))

let respuesta = letra1

if (respuesta > letra2){
    alert("el numero es mayor que a")
}else if( respuesta < letra2){
    alert("el numero es menor que a")
}else {
    alert("el numero es igual que a")
}